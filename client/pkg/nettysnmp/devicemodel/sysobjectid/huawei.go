func ExtractHuaweiDeviceModel(sysDescr string) *devicemodel.DeviceModel {
	deviceType := devicemodel.UnknownDeviceModel
	if strings.Contains(sysDescr, "HUAWEI") {
		for _, line := range strings.Split(sysDescr, "\n") {
			if strings.Contains(line, "HUAWEI") {
				deviceType = strings.TrimSpace(strings.Split(line, "HUAWEI")[1])
			}
		}
	} else if strings.Contains(sysDescr, "Huawei") {
		lines := strings.Split(sysDescr, "\n")
		if len(lines) > 1 {
			deviceType = strings.TrimSpace(lines[0])
		} else {
			deviceType = strings.TrimSpace(strings.Split(lines[0], " ")[1])
		}
	}
	return &devicemodel.DeviceModel{Platform: platform.Huawei, Manufacturer: manufacturer.Huawei, DeviceModel: deviceType}
}
